package model;

/**
 * =============================================================
 * ENUM: LeaveType
 * =============================================================
 *
 * PURPOSE
 * Defines the available leave categories in the
 * MotorPH Payroll System.
 *
 * TYPES
 * -------------------------------------------------------------
 * SICK       → Medical leave
 * VACATION   → Personal vacation leave
 * EMERGENCY  → Urgent personal matters
 * UNPAID     → Leave without salary deduction coverage
 */

public enum LeaveType {

    SICK,
    VACATION,
    EMERGENCY,
    UNPAID

}